/*
    — console.log()
    
        ↪ Método "log" que está dentro objeto "console".
        ↪ Esta função apenas é exibida no "console" do Sistema Operacional ou do navegador.
    
    No javascrit o ponto-e-vírgula é opcional.
    A função console.log() dá uma quebra de linha altomaticamente.
    As crases (``) são utilizadas para acriar "tamplate string".
    O JS é uma linguagem de tipagem dinâmica.
    Em JS não há a separação para números, inteiros ou de ponto flutuante, tudo são "numbers".
*/


// Valores literais.

// Strings:
console.log('Erick Ferreira');
console.log("Programador Web");
console.log(`JavaScript & Typescript`);
console.log();


// Numbers:
console.log(123456); // Inteiro
console.log();

console.log(1234.56); // Ponto flutuante
console.log();


// Tudo junto:
console.log('Erick', 2026, 6547.45);
console.log();